		<img src="android_shot.jpg" id="imgAfficherPlus"><br>
		Si ce n’est pas déjà fait, dépêchez-vous d’installer Shazam ! Pour les fans de bonne musique, c’est une application indispensable : Shazam est en effet capable d’identifier n’importe quelle chanson et de vous renseigner sur le titre, son interprète et l’album. Mieux, l’application va même vous donner quelques liens pour réécouter la musique plus tard. Si vous êtes sur Spotify, Shazam peut même stocker les musiques repérées dans une playlist dédiée à cet effet. Bref, c’est un must !<br>










		

