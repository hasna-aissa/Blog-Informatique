		<img src="Zedge_1352897570.jpg" id="imgAfficherPlus"><br>
		Une fois que vous aurez joué un peu avec votre nouveau smartphone, vous aurez tout naturellement envie de le customiser et de le personnaliser. Afin de lui donner une allure inimitable, vous l’avez déjà peut-être équipé d’une coque de smartphone originale. Pour que votre précieux soit aussi beau à l’intérieur qu’à l’extérieur, on vous conseille de télécharger Zedge. Il s’agit de l’application idéale pour trouver un fond d’écran, une sonnerie originale ou d’autres éléments de customisation. Cerise sur le gâteau : Zedge est totalement gratuit !<br><br>
		<strong>Swiftkey</strong><br>
		<img src="swiftkey_flow.jpg" id="imgAfficherPlus"><br>
