		<p id="articles" >
	Nous sommes toujours plus éloignés. Pour le travail, le voyage, les vacances, la famille… La société moderne creuse des distances géographiques que les nouvelles technologies s’efforcent de combler. Ce qui peut nous manquer le plus, c’est de voir nos proches. Le chat vidéo permet de lutter contre cette absence en nous aidant à discuter avec eux de façon plus personnelle et plus intime. Mais quelle est la meilleure application pour ça ? Découvrez notre sélection d’applications de chat vidéo pour votre smartphone Android.<br><br>
	<strong>Facebook Messenger</strong><br>
	<img src="08387004-photo-facebook-messenger-ban-1024x536.jpg" id="imgAfficherPlus"><br>
	Difficile de faire l’impasse sur l’application de messagerie n°1 en terme d’utilisateurs et qui propose un service vidéo de relativement bonne qualité. Si Facebook Messenger a encore clairement du chemin à faire avant d’être une application parfaite, elle reste incontournable pour l’instant. Et ce n’est sans doute pas prêt de s’arrêter puisque le chat vidéo est déjà en place depuis 2015 sur mobile.
<br>
A noter que Facebook, qui possède aussi Whatsapp (voir ci-dessous), réfléchit à lancer deux autres applications de chat vidéo. D’un côté Bonfire, qui s’inspire de HouseParty, qui cible particulièrement les jeunes avec des discussions de groupes optimisées. De l’autre, Talk. Le public ciblé par cette dernière est les grands-parents pour qu’ils puissent parler facilement avec leurs petits-enfants.<br><br>
<strong>Whatsapp</strong><br>
<img src="whatsapp.jpg" id="imgAfficherPlus"><br>
Un autre nom, mais une même entreprise. Avec le rachat de Facebook, Whatsapp a mis la main sur une pépite devenue depuis la seconde application de messagerie la plus utilisée. Le service a intégré le chat vidéo pour tous les utilisateurs en fin d’année dernière. Difficile d’être sûrs que cela ait joué un rôle pour le cap du milliard d’utilisateurs quotidien atteint fin juillet, mais on peut le penser.<br>

Grâce à la logique de Whatsapp, « vous êtes votre numéro », le service est facile d’utilisation et plutôt stable. Il constitue donc logiquement l’une des meilleures solutions de chat vidéo sur mobile.<br><br>
<strong>Google Duo</strong><br>
<img src="google-duo-100-millions-1024x576.jpg" id="imgAfficherPlus"><br>
Google Duo n’est pas forcément le service le plus connu du géant de Mountain View. Après une grosse mise à jour en fin d’année dernière, l’application a confirmé tout le bien qu’on pouvait penser d’elle en terme de rapidité et de stabilité. Réponse à Facetime d’iOS, il a l’avantage d’être utilisable sur toutes les plateformes. Mais, ce sont les utilisateurs qui font le service. Et pour l’instant, ils ne sont pas au rendez-vous..<br>

A noter la fonction « Knock Knock » qui vous permettra de savoir ce que fait votre interlocuteur avant de lancer la vidéo. Plutôt utile.  Vous pouvez aussi tester l’application très proche Allo.<br>




	
