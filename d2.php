			La création de site web évolue au fil des années et des tendances.  Vous trouverez dans cet article 10 tendances à suivre si vous souhaitez créer un site web moderne et attractif pour trouver encore plus de clients pour votre Entreprise.<br><br>
			<strong style="color: darkorange">1 / Les sites web avec animations déclenchées </strong>	<br>
			<img src="site-web-avec-animation.jpg" id="imgAfficherPlus"><br>
			Les agences web et les créateurs de site web suivent l’évolution des moteurs de recherche pour proposer non plus des images statiques, mais bel et bien des animations qui vont véritablement permettre au visiteur de bénéficier d’une expérience unique et plus engageante lorsqu’il visite un site web.<br>
Voici un exemple de site web avec animations déclenchées : http://www.elmonstruo.org/<br>
Lorsque vous bougez la souris et que vous vous déplacez sur le site, l’expérience utilisateur évolue.<br>
Les animations sont un excellent moyen pour engager et faire entrer l’utilisateur dans l’univers de votre marque.<br>
Il y a une véritable interaction entre l’internaute et le site.<br>
Cela garantit donc une visite plus agréable et plus durable.<br>
Le lecteur du site va voir envie de découvrir les nouvelles animations au fil de la lecture du site et de la souris qui bouge (plus d’engagement).<br>
Elles ont pour effet d’augmenter également la conversion.<br>
Le site n’est plus figé mais il évolue en faisant apparaitre et disparaitre des éléments au fil du temps.
<br><br>
<strong style="color: darkorange">2 / Site web avec priorité au Mobile </strong>	<br>
			<img src="création-site-web-priorité-mobile (1).jpg" id="imgAfficherPlus"><br>
			Dans le jargon du web nous appelons cela le mobile first. Déjà en 2016 43,6 % des visites sur un site web venaient du mobile…
<br>
C’est d’autant plus vrai de nos jours, l’importance de la conception d’un site web adapté au mobile est primordiale et ne cesse d’augmenter d’années en années.
<br>
Depuis le premier semestre 2018, Google vient de déployer son nouvel index mobile first qui accorde plus d’importance à la version mobile de votre site web qu’a celle d’un ordinateur. Votre site web sera plus visible si il est adapté au mobile. Ce qui veut dire qu’il faut créer un site web responsive.
<br>
Les pages AMP (mobile accélérées)sont désormais essentielles. Elles permettent de charger un site rapidement sur mobile.
<br>
Que ce soit pour créer un site web ou refaire un site internet, L’AMP doit être au coeur de vos priorités.<br><br>
<strong style="color: darkorange">3 / Site web avec couleurs vives </strong><br>
<img src="création-site-web-couleurs-vives.jpg" id="imgAfficherPlus"><br>
Si avant la tendance était aux couleurs standards du web, sachez que depuis quelques mois, les couleurs vives et originales pour un site web sont dans les tops tendances en terme de webdesign.<br>
L’audace est devenu importante lorsque l’on crée un site web, il n’est plus du tout étonnant de voir des couleurs flashys sur un site web.
<br>
Les nouvelles technologies en terme d’écrans y sont pour beaucoup. Pour une entreprise qui veut attirer l’attention, les couleurs vibrantes sont un excellent moyen d’y parvenir.
<br>
L’aspect artistique permet également de se différencier de ses concurrents.<br>

