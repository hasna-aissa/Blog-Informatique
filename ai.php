			<strong>Des IA qui parlent entre elles</strong><br>
			Sans aller jusqu’à évoquer des intelligences artificielles capables de converser entre elles ou de livrer des réponses à nos questionnements les plus profonds, les IA sont en mesure de s’entraider. Des recherches ont montré que deux outils pouvaient agir de concert dans l’unique but d’atteindre un objectif plus rapidement.<br>
Les travaux publiés sur le site openAI ont tendu à démontrer qu’il était possible d’entrainer des intelligences dans un unique but : la communication. Les développeurs ont donc appris aux machines à se créer leur propre langage rudimentaire afin qu’ils puissent s’échanger des informations capitales pour mener à bien leur objectif. Pour une mission simple, du type rejoindre le point A, deux capteurs situés à différents points d’une carte vont être en mesure de dialoguer entre eux. L’un va expliquer à l’autre qu’il est le plus proche du but, et que c’est à lui qu’il revient d’exécuter l’ordre de rejoindre l’arrivée.<br>
<strong>L’IA au centre de nos émotions</strong><br>
Les IA seront à même de mieux nous comprendre. Tout du moins, interpréter nos visages et d’analyser notre langage corporel. Des recherches sur la prévention de la fatigue au volant ont mis en avant la capacité des machines à prendre en compte non seulement les éléments physiologiques (clignement des yeux, baisse du regard) que psychologiques.<br><br>

<img src="fatigue-au-volant.jpg" id="imgAfficherPlus"><br>
<strong>Soigner l’humain : quand l’IA s’intéresse « au soi »</strong><br>
La médecine devrait rester un domaine dans lequel l’intelligence artificielle peut soutenir l’expertise des praticiens. En matière de détection des pneumonies, des chercheurs sont parvenus à mieux détecter et anticiper les signes avant-coureurs. La technique est classique puisqu’ils ont nourri leur machine avec plus de 100 000 clichés aux rayons X pour qu’elle soit en mesure d’extraire des routines de détection. A terme, l’intelligence artificielle ainsi formée peut s’exercer sur pas moins de 14 autres maladies.<br>
La santé mentale devrait également être un terrain d’expérimentation. En 2017, un chatbot du nom de Woebot (disponible sur Facebook Messenger) fut censé faire office de thérapeute. Il s’agit alors davantage d’un robot conversationnel qu’un véritable outil de soin. Toutefois, Woebot ouvre une porte dans le domaine de l’assistance aux personnes les plus fragiles.<br>


	
