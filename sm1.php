		<h2 id="title_articles">QUELS SONT LES 5 RÉSEAUX SOCIAUX DU MOMENT POUR LES JEUNES ?</h2><br><br>
		<img src="strats-image-4014856.jpeg" id="imgAfficherPlus"><br>
		<p id="articles" >
			Malgré les polémiques à répétition, Facebook reste le maître absolu mais perd du terrain chez les plus jeunes, au profit de Snapchat ou YouTube mais aussi de sa propre filiale Instagram.<br>
			<strong>Facebook, le roi</strong><br>
			Avec 2,2 milliards d'usagers mensuels actifs (MAU) fin mars, le groupe de Mark Zuckerberg règne en maître. Ce chiffre est scruté de près par les marchés financiers car de lui dépendent les recettes publicitaires, qui forment la quasi-totalité des revenus du groupe. Les MAU ont toujours progressé depuis la naissance du réseau. Selon le cabinet Forrester, les ados internautes américains (12-17 ans) étaient en 2017 76% à utiliser Facebook contre 78% en 2014, un repli qui contraste avec YouTube (96% en 2017 contre 87% en 2014), Instagram (69% contre 52%) ou encore Snapchat (67% contre 39%). En 2017, il a engrangé un chiffre d'affaires de près de 41 milliards de dollars pour un bénéfice de 16 milliards. eMarketer prévoit que Facebook perde de 2 millions d'utilisateurs américains de moins de 24 ans cette année tandis qu'Instagram devrait au contraire engranger 1,6 million d'usagers supplémentaires aux Etats-Unis de moins de 24 ans et Snapchat 1,9.<br>
			<strong>Instagram, la filiale qui monte</strong><br>
Créé en 2010 puis racheté par Facebook en 2012 pour un milliard de dollars, Instagram, célèbre au départ pour ses photos carrées et ses filtres rétro, vient d'annoncer avoir dépassé le seuil du milliard d'utilisateurs. Il vient de lancer une nouvelle plateforme, IGTV, entièrement dédiée à des vidéos, de format allant de 10 minutes à une heure, contre une minute auparavant, ce qui devrait continuer à séduire le jeune public. Instagram est très utilisé par des artistes, des marques, des entreprises, qui l'utilisent comme vitrine et lien direct avec les internautes ou y achètent des espaces publicitaires. Selon le cabinet eMarketer, rien qu'aux Etats-Unis, Instagram va générer cette année 5,48 milliards de dollars en recettes publicitaires, un bond de plus de 70% sur un an. Il représente 28,2% des recettes publicitaires mobiles du groupe Facebook, un chiffre qui pourrait atteindre 40% en 2020.<br>
			<strong> L'incontournable Snapchat</strong><br>
			Née en 2011, l'application était d'abord un service de messagerie connu pour ses messages photo ou vidéo disparaissant au bout d'un certain temps. Snapchat est devenu au fil du temps une application qui comprend de la vidéo, des actualités et d'autres contenus. Fin mars, Snapchat revendiquait 191 millions d'utilisateurs quotidiens dans le monde. Il a réalisé un chiffre d'affaires de 231 millions de dollars au premier trimestre mais accusé une perte de 386 millions, toutefois fortement réduite par rapport à l'année dernière.<br>
			<strong>YouTube, le géant de la vidéo</strong><br>
			Créée en 2005 puis achetée par Google l'année suivante pour 1,65 milliard de dollars. Plateforme de partage de vidéos amateur au départ, ses contenus se sont largement professionnalisés depuis. De nombreux médias ou entreprises y publient des contenus, souvent via des chaînes dédiées. Certains sont devenus célèbres grâce à leurs vidéos sur YouTube (musique, sketches, conseils de cuisine ou de maquillage...), donnant naissance au terme "YouTubeur". Le groupe revendique 1,9 milliard d'usagers, sans compter toutes les personnes regardant YouTube mais sans avoir de compte. YouTube se décline aussi en musique et en version payante sans publicité.<br>
			<strong> Twitter et les autres</strong><br>
			Avec 336 millions d'usagers mensuels, Twitter vient seulement de commencer à être bénéficiaire. Il est moins prisé des jeunes que les autres réseaux sociaux car il est moins intime (les messages sont lisibles par tous les usagers) mais progresse selon Forrester avec 53% des internautes adolescents l'utilisant en 2017 contre 47% en 2014. Les messageries WhatsApp (sans pub) et Messenger (avec pub), également détenues par Facebook, dépassent le milliard d'utilisateurs.<br>

		

			


	
