Une stratégie inefficace si le pays de l’oncle Sam veut garder une distance d’avance sur la Chine, dont les investissements massifs montrent une détermination sans faille à devenir le leader mondial de l’IA.<br>
Le sous-comité multiplie les recommandations à l’administration Trump afin d’éviter un monde où l’intelligence artificielle chinoise règne en maître. Mais elles requièrent une « action immédiate ». À moins que le président Trump ne réalise que de telles technologies sont primordiales, la Chine deviendra leader mondial avant les nouvelles élections présidentielles.<br>

			


	
