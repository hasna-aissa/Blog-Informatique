			L'Intelligence artificielle devient un axe majeur de développement pour les grandes puissances du monde et le gouvernement chinois mise beaucoup dessus.<br>
			L’intelligence artificielle représentera bientôt un enjeu crucial dans le développement et la défense des pays industrialisés et les investissements liés à cette dernière sont scrutés par tous. C’est notamment le cas du sous-comité pour la surveillance et les réformes gouvernementales américain, qui vient de livrer un rapport sur le sujet.<br>
Nommé « L’Avènement des machines : L’intelligence artificielle et son impact grandissant sur la politique américaine », ce document est rédigé par deux membres du Congrès : le républicain texan Will Hurd et le démocrate de l’Illinois Robin Kelly. Les deux hommes ont interviewé des experts durant une année et livrent un constat qui leur semble alarmant. La conclusion laisse notamment entendre que les États-Unis n’arrivent pas à suivre le rythme de la Chine.<br>
Le sous-comité reconnait et approuve le récent engagement de deux milliards de dollars à destination de l’IA pour la défense, mais insiste pour préciser que cela ne sera pas assez. Le document indique que le budget de la R&D chinois devrait dépasser celui des USA d’ici la fin de l’année 2018. Durant les neuf derniers mois, les États-Unis n’ont fait que suivre le flux d’investissement chinois, bien que le secteur privé se maintienne en pole position.<br>
