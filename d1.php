		<p id="articles" >
			En tant que supports numériques, les sites web sont extrêmement susceptibles d’évoluer en 2018, bien plus que les techniques traditionnelles d’imprimerie… Mais, 18 années après le début du millénaire, ça ne surprend plus grand monde, non ? Ce qui est surprenant, en revanche, c’est la manière dont les designers web ont réussi à s’adapter aux défis techniques les uns après les autres, tout en parvenant à créer des sites intuitifs, clairs, innovants, cohérents avec les marques qu’ils représentent, adaptatifs pour aller sur tous les formats d’écrans et tout simplement beaux.<br>
Beaucoup de nouveautés ont vu le jour en 2017, y compris l’utilisation mobile qui a pris le dessus sur toutes les autres. Cela signifie que 2018 va devoir exploiter les fonctionnalités plus que jamais, tandis que l’utilisation bureau devra évoluer pour rester dans la course. Avec tout ça en tête, voyons voir ce que les tendances en design web nous réservent pour 2018.
				<br>
				<strong style="text-shadow: 10px;">Tendances design web à surveiller de près en 2018</strong><br><br>
				<strong style="color: darkorange">1. Ombres et profondeur</strong><br>
				<img src="algolia.com_-2.png" id="imgAfficherPlus"><br>
				L’utilisation des ombres n’est pas toute nouvelle. Pourquoi donc en parler ? Bien que cette technique existe depuis un certain temps dans le monde du design web, on voit émerger de nouvelles variations, notamment grâce à l’évolution des navigateurs. Les mises en page parallaxe ou en grille permettent aux graphistes de jouer avec les ombres au maximum de sorte à donner de la profondeur à leurs design et créer ainsi l’illusion que tout un monde se cache derrière l’écran. C’est l’une des réactions directes à la tendance flat design très à la mode ces dernières années.<br><br>
				<strong style="color: darkorange">2. Palettes de couleurs saturées</strong><br>
				<img src="adobe.png" id="imgAfficherPlus"><br>
				2018 est clairement l’année de tous les excès en ce qui concerne les couleurs sur le net. Si auparavant les marques et les graphistes préféraient faire des choix sans risques, de plus en plus de designers tentent le tout pour le tout avec des couleurs plus courageuses, notamment avec des couleurs très saturées. Ces couleurs-là sont généralement utilisées pour les en-têtes qui ne sont d’ailleurs plus « juste horizontales », mais réinventées avec imagination.
<br>
Ceci est notamment aidé par les avancées technologiques qui concernent les moniteurs et les écrans en général qui reproduisent plus efficacement les couleurs riches. Les tons vibrants, et même les couleurs contrastantes, peuvent être très utiles pour les nouvelles marques qui espèrent attirer l’attention de consommateurs très rapidement. Mais c’est également une bonne méthode pour les entreprises déjà établies, pour se démarquer des sites web plus traditionnels.<br><br>
<strong style="color: darkorange">3. Illustrations sur mesure</strong><br>
				<img src="stride.com_-1.png" id="imgAfficherPlus"><br>
