<strong style="color: darkorange">2. Inventaire publicitaire de Facebook</strong><br>
Facebook doit faire face à des problèmes plus urgents, tels qu'un inventaire publicitaire en diminution. Tout simplement, il manque d'espace pour afficher des publicités. La société a confirmé début 2017 qu'elle maximisait la diffusion des annonces dans les nouveaux flux. Elle a plutôt choisi de hiérarchiser les vidéos les plus longues dans le calendrier afin d'améliorer l'expérience.
<br>
Facebook a ensuite introduit des publicités mid-roll dans ces vidéos, ce qui lui confère une toute nouvelle source de revenus. Elle a également commencé à vendre des annonces Messenger , tout en ouvrant sa plate-forme Marketplace aux annonceurs.
<br>
Les revenus publicitaires de la société pour le troisième trimestre ont ralenti en glissement annuel, passant de 59% en 2016 à 47% cette année, ce qui fait peut-être partie d'une stratégie maîtrisée mais conduira toujours inévitablement à des tentatives d'accélération de la croissance en 2018. surveillez de près les mises à jour en provenance de Menlo Park.
