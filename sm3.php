<strong style="color: darkorange">1. WeChat en hausse</strong><br>
Si Facebook pouvait envisager son avenir dans une boule de cristal, cela pourrait ressembler à WeChat. Le géant chinois de la messagerie s'est rendu indispensable pour ses utilisateurs et, selon Gartner, est utilisé par 95% des Chinois âgés de 16 ans et plus.<br>

Derrière son succès étonnant se cache sa capacité à être tout pour tous. Vous pouvez l'utiliser pour consulter votre compte bancaire, commander un taxi, acheter de la nourriture ou même discuter avec des collègues sur le lieu de travail. Il s'agit de quatre applications distinctes qu'il a remplacées sur place, et nous avons à peine effleuré la surface de ce qu'il peut faire.<br>

Le fait le plus significatif est que le fait de disposer d'une telle offre multicouche procure à WeChat plus de revenus potentiels.<br>

Facebook tente de copier ce modèle commercial lucratif, en poussant Messenger de manière plus agressive sur ses utilisateurs et en achetant Whatsapp ont été les premières étapes de ce processus. Que les gens soient prêts à accepter Facebook comme une «super application» de style WeChat est discutable, mais il sera intéressant de voir si nous nous rapprochons de l'inévitable jeu final de 2018.<br><br>
