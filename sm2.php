		<h2 id="title_articles">Quels sont les médias sociaux les plus populaires?</h2><br><br>
		<img src="3013086-slide-slide-6-who-will-pickup-where-wordpress-left-off.jpg" id="imgAfficherPlus"><br>
		<p id="articles" >
			Vous utilisez sûrement Facebook presque tous les jours, vous avez aussi un compte sur Instagram et un autre sur Twitter. Vous passez une fois de temps en temps mettre à jour votre profil LinkedIn. Mais quels sont réellement les médias sociaux les plus utilisés?<br>
			Le site GlobalWebIndex  a récemment publié des données concernant l’augmentation du nombre de membres sur les principales plateformes web sociales.
<br>
Pinterest se retrouve en tête, avec une augmentation de 57 % du nombre de membres. Ce site permet d’épingler et de partager des images avec ses abonnés et de classer ses images par tableaux.<br>

Le site de microblogage, Tumblr, arrive second avec une augmentation de 45 % de membres. La popularité des .gif animés qu’on retrouve en grande quantité sur ces blogues en est peut-être la cause.<br>

Enfin, Instagram et LinkedIn arrivent respectivement troisième (36 %) et quatrième (27 %). Facebook est bon dernier avec une augmentation de seulement 6 %. Il faut dire que le site compte déjà énormément de membres, ce qui réduit son taux d’augmentation.
<br>
Il est aussi intéressant de comparer les utilisateurs actifs. Ce sont ceux qui se sont connectés ou qui ont contribué à la plateforme (partage de contenu, commentaires, « likes », etc.). Même sous cet angle, Facebook reste dernier avec une faible augmentation de 2 %. Cependant, les utilisateurs sont déjà bien actifs sur la plateforme, ce qui fait stagner l’augmentation (voir plus loin).
<br>
En tête, le constat est assez similaire aux données sur le nombre de membres. Tumblr et Pinterest qui s’échangent la première position. Le site de microblogage présente une augmentation de 120 % de ses utilisateurs actifs, alors que Pinterest montre une augmentation de 111 %. Encore une fois, Instagram et LinkedIn sont en troisième et quatrième positions.
<br>
Twitter et YouTube, déjà bien implantés, ont tout de même une augmentation d’utilisateurs actifs d’environ 25 % chacun.
