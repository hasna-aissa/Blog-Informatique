On peut les utiliser à différentes étapes de l’apprentissage.Ce sont les mêmes applications qui sont utilisées de façon individuelle avec tablettes des élèves, ou en utilisation collective sur un écran interactif contenant un environnement Android, ce qui permet à tout le monde de pouvoir profiter d’une séance d’exercice.<br>
