		Dans tous les secteurs d’activités, les nouvelles technologies se sont faits une place de choix. Les tablettes, écrans interactifs et autres solutions interactives contribuent à l’amélioration de l’éducation grâce aux ressources pédagogiques que sont les applications. Ces dernières sont nombreuses, innombrables même. Il peut être fastidieux pour un enseignant de faire le travail de recherche des meilleures applications pour sa classe, sachant que le nombre d’application sur Google Play gagne régulièrement en nombre.<br>

